# Load packages -----
library(readr)
library(dplyr)
library(tidymodels)

# Read data ------
names <- 
  read_csv("spambase/spambase.names", 
         skip = 32,
         col_names = FALSE) %>% 
  separate(X1,
           into = c("name", "drop"),
           sep = ":") %>% 
  select(-drop) %>% 
  bind_rows(tibble(name = "spam")) %>% 
  pull
  

spam <- read_csv("spambase/spambase.data", col_names = names)


